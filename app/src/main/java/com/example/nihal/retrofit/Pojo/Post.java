package com.example.nihal.retrofit.Pojo;

public class Post {

    String title;
    String id;
    String image;
    String platform;
    String date;


    public String getTitle() {
        return title;
    }

    public String getId() {
        return id;
    }

    public String getImage() {
        return image;
    }

    public String getPlatform() {
        return platform;
    }

    public String getDate() {
        return date;
    }
}
